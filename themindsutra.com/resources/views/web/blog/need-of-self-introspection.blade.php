                <p>Evaluating our daily lives acts as a moral compass for our life,and guides us on a path that will help us lead a life we could be proud of.</p>
                <p>Researchers have shown that we think more than 50,000 thoughts per day, of which more than half are negative and more than 90% are just repeats from the day before. It is the basis of cognitive therapy behaviour also which is the mainstay of psychotherapy where one dedicates time for introspection through journalling exercises in order to refocus in life.</p>
                <p>The same goes for Bill Gates. He attributes his periodic ritual of isolation, that he calls his “Think Week, ” as a time when he engages in active pondering, letting new ideas flood into the quiet space he's cleared for them.</p>
                <p>For decades, Gates has been embarking on biannual seven-day retreats to an isolated two storey cabin in the Pacific island. While there, he disconnects from all technology and cuts himself off from all family, friends and staff . This cocoon of solitude gives him a chance to look at his life from a higher perspective. He has found that this separation gives him clarity and the ability to think outside the box.</p>
                <p>Don't we all need this introspection and self analysis?</p>
                <p>Whether we do it in isolation as Bill Gates does or as a daily ritual as Benjamin Franklin did, the fact remains that this time of self-evaluation is necessary if we wish to be even slightly different from who we are today.</p>
                <p>If we don't make the time and effort to refocus our mind on the positive through introspection, we won't give ourselves the opportunity to grow and develop.</p>
                <p>These practices when implemented collectively at the workplace bring amazing warmth and camaraderie.  While I was training in the psychiatry department at CMC Vellore, we had a reflection meeting at CMC Vellore at the end of the day wherein we discussed our lessons and challenges of that day. It was an amazing collective insightful discussion by all team members and it would help change our line of thought. Things we couldn't decipher or thoughts that troubled us for the day were answered and it would build a strong sense of bonding in the team at the end of the day which would change our relationship with our work.</p>
                <p>Shaifali witnessed a similar scenario at Sri Sathya Sai Institute of Higher Medical Sciences, Bangalore. Every patient who came to the hospital underwent BRMC  procedure ie. Body relaxation , Mental Cleansing procedure. It goes as follows: Each patient individually or in a small group is asked to relax with eyes closed and suggestions are given to him to breathe in and a relaxed manner. These suggestions of letting go of their anxieties ,reminding them of their strength and asking them to embrace strength , positivity and reminding them that the Universe does only the best for them.</p>
                <p>Almost always, the patients share how relaxed they feel and are very thankful for the connection.</p>
                <p>The patients and their relatives are asked to repeat this exercise 2-3 times a day during their stay at the hospital and meet the compassionate team during their stay at the hospital.</p>
                <p>The staff also says a prayer together each morning , surrendering their  stresses and anxieties and asking for strength to do their best.</p>
                <p>This reminder to an inspiring positive mindset, a paradigm shift to a higher perspective is so relevant whether at the start or at the end of the day , at  the hospital , at our workplace or even when at home alone or with the family.</p>
                <p>Naikan, a method of quiet self-reflection, is a Japanese word that means “looking inside,” though a more poetic translation might be “seeing oneself with the mind’s eye.” It is a structured method of self-reflection that helps us to understand ourselves, our relationships, and life’s mysteries and miracles.</p>
                <p>Naikan reflection is based on three questions:</p>
                <p>What have I received from ____?</p>
                <p>What have I given to ____?</p>
                <p>What troubles and difficulties have I caused ____?</p>
