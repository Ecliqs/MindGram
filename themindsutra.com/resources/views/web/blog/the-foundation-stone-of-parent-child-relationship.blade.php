                <p>Ahimsa, or non-harming, is perhaps the Yama all parents struggle to achieve consistently. How many times have you treated your own child in a harmful way? Maybe speaking a little too harshly, failing to fully acknowledge their feelings, or being too rushed to hear their little voices.</p>
                <p>To control, to worry is such a natural reaction when parenting. Why can't  we just let our kids be. Isn't worrying and nagging a kind of violence we do to our children as well. Why can't we just let go and let be.</p>
                <p>As tempting as it is to help a child succeed, parents who hover or are overly involved in their child's life can do more harm than good.</p>
                <p>Post the lockdown, students entering high school who have been home for a year during the pandemic are suddenly being pushed by well-meaning ambitious parents to join the coaching classes for entrance exams for FIITJEE and NEET ( engineering and medical entrance exams in India). There is a demanding environment at these classes and at home, which pushed is patient of mine and others to attempt suicide. The competitive and violent environment at these classes pushes them to suicidal ideation.</p>
                <p>Hovering around the kids or handling their chores leave kids without deductive reasoning. That, in turn, stunts a child's ability to resolve challenging scenarios creatively when they encounter problems.</p>
                <p>Ahimsa invites us to step back and see if we are putting our kids through mental stress unknowingly. Practicing Ahimsa means to come from a place of honest compassion and Love and becoming conscious of how subtle negative thoughts and responses can hurt and thus let them go.</p>
