                <p>There was a time when I had become defensive in life. During those times, when we are unable to assert ourselves, there arises an existential fear which pushes us to react, and cause violence to ourselves and those around us. I had then become reactive whenever my space got compromised. My self-talk had turned negative as well. I was hurting myself by the negative bashing I gave myself.</p>
                <p>Often when we are without a purpose to drive us, we open ourselves to the opinion of others and criticism. We take offence for almost everything that gets spoken and we see ourselves as victims. When I look at my thoughts a few years back, I realise how I had pushed myself into the victim mode and therefore reacted defensively at work. As my reflections deepened I realised when we cause injury to others, we harm ourselves, as well.</p>
                <p>The ability to solve a problem constructively is the real art and science learnt with yoga and psychology. It was only with self awareness that I recognised certain patterns of thinking and noticed that I still had that voice that was so quick to berate some issues and magnify others. Later as I read up about non violence and reasons that give rise to anger, I learnt that existential fear leads to anger and any kind violence. When everything we identify as our own is challenged, we feel a strong need to defend which comes out as anger. As I started challenging my thought patterns , I realised that opposite if offended is not defended, it’s assertion.For me like for most,  Assertion began in small steps- slight change in thoughts and behaviour. I would tell my negative thoughts” I know there is wrong being done to me but entertaining you in my mind and heart, doesnot help. So, I am not allowing you in my life,” l started to respond in a firm (but as my friends say) and gentle  ‘No’. And slowly but steadily, assertiveness replaced defensiveness in my life. This is how I define assertion, “ Saying a humble and confident  yes to thoughts and people I want more of in my life. Also, a firm but gentle No to thoughts and people I want less of in my life.”</p>
