                <p>At a very subtle level, every person has an invisible border that sets their comfort level when interacting with others. Ahimsa, the foundation stone of Yamas, guides us to create and safeguard these boundaries.  Asteya, the third gem of Yama guides us not to trespass on these boundaries.</p>
                <p>Every interaction of ours with the other people or the environment involves us to give and take. This might be unavoidable in the business of living. Asteya, the gem of non-stealing, guides us to take what is needed for our well-being and growth, but mindfully and not mindlessly.</p>
                <p>In 1998, Bill Gates, chairman of the world's largest software company Microsoft, was sued for stifling competition in order to create a monopoly. He was held guilty of violating federal antitrust laws. This probably led Gates to introspect and realize that power and money wasn't really giving him joy and peace. This might have led him to take up a full-time philanthropic role in the Bill & Melinda Gates Foundation. Today, Bill Gates is known as one of the most generous philanthropists for his work toward the betterment of the world.</p>
                <p>I cannot help but compare Gates' journey from conqueror to renouncer of wealth to that of King Ashoka from ancient India. As we turn the pages of history, we recall that King Ashoka, after conquering Kalinga, the last Indian state to resist his sovereignty, was moved into a deep remorse. The sufferings that the war inflicted on the defeated people moved him to such an extent that he renounced armed conquests and dedicated his life to spreading the teachings of Buddha.</p>
                <p>Probably, the reason for the change of heart in Bill Gates and Ashoka was the realisation that in stealing from others, we eventually steal our own self. There is no intrinsic evil in wealth or power, but in the way we acquire or use it determines its intrinsic worth.</p>
                <p>In today's world, creative ideas are often copied completely and intellectual rights are violated. Stealing from the other without acknowledgment hurts the other and is an offense. We may steal creative ideas and intellectual research for a new business or project. The need to steal essentially arises because of a lack of confidence in our ability to create what we need by ourselves. However, if the same ideas are adapted with transparency and after acknowledgment to the other, it results in joy, growth and creative synergy.</p>
                <p>I had designed the art therapy course, and this was used by someone I had shared and worked with at that moment of time without my permission. It has made me more conscious about acknowledging others for their creative work while taking the effort to protect my own. This has led to better working relationships with my partners and greater creative synergy.</p>
