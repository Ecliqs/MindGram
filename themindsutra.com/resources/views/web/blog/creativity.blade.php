                <p>Mihaly Csikszentmihalyi, the author of the book “Creativity: The Psychology of Discovery and Invention,” gives a pretty hefty definition of the word. He said, “Creativity is a central source of meaning in our lives … most of the things that are interesting, important, and human are the results of creativity… [and] when we are involved in it, we feel that we are living more fully than during the rest of life.”</p>
                <p>Referring to a person in the middle of the creative process Csikszentmihaly said, “He can't feel even that he's hungry or tired, his body disappears, his identity disappears from his consciousness because he doesn't have enough attention, like none of us do, to really do well something that requires a lot of concentration and at the same time to feel that he exists.”</p>
                <p>When we are completely involved with creating something new, we don't pay attention to how our body feels or any problems we may be having at home.</p>
                <p>As playwright George Bernard Shaw sums it up, “Some men see things as they are and say 'why?'' I dream of things that never were and say 'why not.'”</p>
                <p>We are all familiar with the experience of getting a sudden creative insight in the shower, and psychologists have studied the brain basis for why such enjoyable idling spurs creativity. The answer seems to be a combination of dopamine and a calm or a non preoccupied mind.</p>
