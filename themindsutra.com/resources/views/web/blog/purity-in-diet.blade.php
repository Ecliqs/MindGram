                <p>Gandhiji believed that food was an integral part of shaping our consciousness and not just meant to satiate our hunger. He experimented throughout his life to find his 'perfect diet', an experiment that lasted over 35 years.</p>
                <p>Mitahara is a concept in Indian philosophy that integrates awareness about food, drink, balanced diet and consumption habits and its effect on one's body and mind. Mitahara literally means the habit of moderate food and is one of the ten yamas in ancient Indian texts.</p>
                <p>The food that we eat, not only influences our health physically, but also influences our emotions and thoughts. Yoga does not dissect food into proteins, carbohydrates or fats, instead it classifies it into three classes – satva, rajas and tamas, based on the effect it has on our mind and body.  Tamasic food is the kind of food which makes us lethargic or sluggish,  Rajasic food is that which brings about activity or restlessness and Satvic food makes us feel light, energetic and enthusiastic.</p>
                <p>Hatha Yoga Pradipika recommends that one must “eat only when one feels hungry” and “neither overeat nor eat to completely fill the capacity of one’s stomach; rather leave a quarter portion empty and fill three quarters with quality food and fresh water”.</p>
                <p>The Yogic science strongly recommends a pure or satvic diet as a method of cleansing ourselves. A satvic diet shares the qualities of satva, some of which include pure, natural, energy-containing, clean, conscious, true, honest. A satvic diet can also exemplify Ahimsa, the principle of not causing harm to other living beings. This is one of the reasons yogis often follow a vegetarian diet.</p>
