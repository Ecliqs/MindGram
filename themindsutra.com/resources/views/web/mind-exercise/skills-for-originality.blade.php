                    <p><a href="#">Exercises</a> to develop self-control.</p>
                    <p>I love the French expression, joie de vivre, which means joy of living or exuberance of spirit. We can only share what we have. One of the most rewarding experiences is to feel and make people feel happy about themselves. Here are some exercises for you to experience joie de vivre and spread it to others.</p>
                    <h3>Week 1: Asteya (non-stealing) exercise for time</h3>
                    <p>Get on a dopamine fast Ask yourself : Do I fritter my time away seeking pleasure with meaningless activities (e.g., spending time on the phone) and yet feel joyless? If yes, you need a detox. Dopamine Detox: Start with a practice of social media detox for about half an hour to one hour. Keep extending it by half an hour every 3rd day, until you can shut off for an entire day or one entire weekend every week. (Tip - plan what you will do in the time you generate by keeping off social media. It's important to plan on what not to do, but it's more important to plan on what to do.)</p>
                    <h3>Week 2: Asteya exercise for self-worth</h3>
                    <p>Prepare a JOMO list Ask: Am I comparing myself with others? Make a list of both materialistic & non-materialistic things you compare yourself with others. Now look at yourself with kindness. What will your best friend or mentor tell you about not having those things/ opportunities? Make a list of all these things. This is your own JOMO list.</p>
                    <h3>Week 3: Asteya (non-stealing) of power from self</h3>
                    <p>Increase Autonomy Look at the areas of your life where you feel powerless. Now look at other areas of your life where you feel completely in control. Now, invest more time to grow this enjoyable activity in your life. Expand areas where you feel in power while trying to spend less mind space on areas outside your control.</p>
                    <h3>Week 4: Asteya exercise for long-lasting joy</h3>
                    <p>Serve others Involve yourself in a charitable activity. Genuinely look at what you can do/give on a regular basis. Charity can begin at home. It may not be about big stuff. It takes little to make people smile. It could be playtime with your kids or dinner date with your parents. Or a service in your local community. Decide on specific days and time and promise to do it regularly.</p>
                    <h3>Week 5: Smile &amp; Appreciate</h3>
                    <p>If we could decide to smile at everybody we meet today. Write 5 letters of appreciation or message  people close to us who we work with and meet everyday.</p>
                    <h3>Week 6: Look at your limitings</h3>
                    <p>Look at 5 things, or more, that you think are limiting you. Look at your attitude, your behaviors, thoughts, patterns, emotions, self-care style, belief systems & other things that can be there to sabotage your wishes & steal your dreams from you, both materially & non-materially. Write them down in your notebook & look at them in a compassionate way. That will start to transform those limitations into a door opener. See how you can act differently.</p>
                    <h3>Week 7: Asteya or non stealing from environment</h3>
                    <ul>
                        <li>Involve yourself in a charitable activity.</li>
                        <li>Genuinely look at what you could do / serve / give on a regular basis.</li>
                        <li>Involve others as then it becomes a sustained activity.</li>
                        <li>Decide on specific days and time and promise to do it regularly. Grow it as you go along.</li>
