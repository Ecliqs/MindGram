                <p>When the railway guard ordered Gandhi to quit the first-class carriage and forcibly ejected him from the train, Gandhi was filled with astonished disbelief and fury. Gandhi responded to this experience by transmuting it into something creative, something redemptive, something that changed the shock and fury into a transformational resolve. That resolve was if non-violence or Ahimsa. Ahimsa helped Gandhi to transform the fury within him into a struggle for redemption of an entire nation. That is the strength of Ahimsa.</p>
