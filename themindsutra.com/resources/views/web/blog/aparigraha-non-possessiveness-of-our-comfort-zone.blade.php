                <p>Often we steal the joy of manifesting the highest versions of ourselves by not exploring our potential further by being so attached to our comfort zones.  Aparigraha or non-attachment basically removes the inertia that we experience in our everyday lives and pushes us to work on our boundaries of comfort zone to express our highest version of ourselves.</p>
                <p>We dissipate our energy in our daily routine that we are used to so that we don't explore our potential or opportunities further or move out of our comfort zones. When we are non attached , we are in a position to move out, to question our routines, our comfort zones, our addictions and move out of them to explore new beginnings.</p>
                <p>Rolo May, the distinguished psychiatrist, shares in his book 'Man's search for himself' that the opposite for courage in our society is not cowardice. It is conformity. Conformity to what all are doing. This may result in stagnation at our jobs at some point of time, but not all of us choose to do something about it.</p>
                <p>Gurudev Sri Sri Ravi Shankar says, “Sometimes we should get out of our comfort zones, then the abilities in us dawn”. When we chase our infinite potential, we are also able to uplift the people around us and help make this world a better place. Let's not steal those opportunities away from people and the world!</p>
