                    <h3>Week 2: Daily emotions checker</h3>
                    <p>Emotional vocabulary exercise A great way to manage unhealthy emotions through self-awareness is the emotional vocabulary. Examine yourself when you are feeling any extreme emotion like anger or guilt. Instead of mulling over the people or situation which caused it, name the emotion in one or two words. Now try to convert this extreme negative emotion into its milder variant. E.g,- Instead of saying 'he makes my blood boil,'' try saying 'I am upset with him'. Practise emotional flexibility whenever you can.</p>
                    <h3>Week 3: Daily self-observation</h3>
                    <p>Self as CCTV camera technique Simply observe your own experience. As if the incident was caught in a CCTV camera and you are observing it on screen. So, rather than being caught up in your experiences or judgements, you are viewing it like a disinterested spectator would. This helps to create distance from your thoughts and emotions so that you can avoid being swept up in them. By doing so, you become more solution oriented rather than problem oriented.</p>
                    <h3>Week 4: Daily satsang practice</h3>
                    <p>Be in good company daily for at least 20 minutes. The good company can be that of friends, books or an online meditation group. Satsang ensures that you dive within to practise active learning and contemplation daily.</p>
                    <h3>Week 5: Self-awareness</h3>
                    <p>A great way to manage unhealthy emotions through self-awareness is the thought naming exercise, where we make a list of all the thoughts that are dominating our mind at this very moment, the people or circumstances that we think may be causing them, and name the emotions in one or two words.</p>
