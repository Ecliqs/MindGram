                <p>Practicing Ahimsa is being assertive in the face of violence. Assertion is a skill that can be developed and practised. I have coined this pnuemonic 'ABCD of Assertiveness' for ease of understanding and oractise. The ABCD of Ahimsa is:</p>
                <ul>
                    <li><b>Awarness: </b>we need to define our boundaries and be able to decipher when we feel violated.</li>
                    <li><b>Best Alternative option: </b>After awareness, we need to examine all the alternatives available. Usually option which are Flor a 'better us' and not 'me vs you' are the best. and then</li>
                    <li><b>Communicate: </b>communicate to the concerned people from a space of calm. Avoid emotionally react with anger, hatred or hurt.</li>
                    <li><b>Decide: </b>Agree on a common ground or agree to disagree. Avoid walking out with unresolved conflicts.</li>
