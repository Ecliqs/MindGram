                    <p>Gratitude is about recognising all the good and positives we have in life, all the beauty that surrounds us and within us. Thanking others, thanking ourselves, Mother Nature for the abundance and joy we see around – gratitude in any form can enlighten the mind and make us feel happier.</p>
                    <h3>Week 1: Mirror Exercise</h3>
                    <p>Appreciate Yourself. Stand in front of your mirror and speak five good things to yourself. It can be about your past achievements or your present efforts, your talents and your virtues. Notice if that makes you feel better. Repeat this as often as you want to and record your experience.</p>
                    <h3>Week 2: Gratitude journaling</h3>
                    <p>Start a gratitude journal. Make this a regular practice. The first hours of waking can shape the rest of your day. Start the day with gratitude. Thank the Universe that you are alive and welcome the day. Turn your attention to the beauty of having a fresh new day and find as many things as possible to be grateful for. Make an endless list - the physical, mental, emotional, and spiritual things. Small things and big things, the obvious or mysterious.</p>
                    <h3>Week 3: Mindfulness exercise</h3>
                    <p>Focus on eating food, like dark chocolate or even a raisin. Eat slowly, utilising all five senses: smell, taste, touch, sight, and even sound. Do this everyday with all meals. Extend awareness to other activities in the day. Find a friend to discuss your new insights on gratitude.</p>
                    <h3>Week 4: Gratitude ritual</h3>
                    <p>Plan a monthly ritual involving your friends, family or colleagues . It could be exchanging handwritten notes or doing a gratitude meditation together. Get creative with your gratitude ritual. Express thankfulness to everyone or everything during this ritual. Share memories and offer your support to others.</p>
                    <h3>Week 5: Find a gratitude buddy</h3>
                    <p>Find a gratitude buddy for your daily practice – it can be your spouse, your kid, or your friend at work. Set aside some minutes everyday where you two (or more if you have more buddies) sit together and discuss the things you are thankful for. Ask questions to each other and open up informally. Sharing thoughts of gratitude with someone is a great way to sustain motivation and strengthen your emotional skills.</p>
                    <h3>Week 6: Positivity</h3>
                    <p>Initiate the plan, go and express your thankfulness to who have offered unconditional support and help – let the person feel important. Make it a monthly ritual. Exchange some good memories and offer your support. In most cases, gratitude visits bring a feeling of sanctity and positivity instantly.</p>
