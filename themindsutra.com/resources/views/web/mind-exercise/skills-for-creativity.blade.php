                    <p>We need to look at ourselves and see if our actions are complicating our relationships? Sorted relationships and peaceful life inspire creativity. So, we need to take a break from our hectic schedule. Create a relaxed state by breathing deeply, stretching, going for a walk, listening to music or whatever that works for us. Once relaxed, we might find a creative answer to a problem that has been eluding you.</p>
