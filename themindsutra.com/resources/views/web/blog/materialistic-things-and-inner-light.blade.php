                <p>These are the times when I feel more sorted and lighter, have more fun and am even more creative.</p>
                <p>While writing this, I understood the dilemma that Krishna ( Universe ) had presented to Arjuna and Duryodhana. It was the same predicament we face each time, when we are given to choose between materialistic things and inner light.</p>
                <p>The story went like this:  Duryodhana had gone to Krishna, to ask for his support in the war. Krishna was taking a nap when Duryodhana reached. So, he took a seat beside Krishna's head and waited for him to wake up. At the same time, Arjuna also came to Krishna to ask him to support the Pandavas in the war. Arjuna was very humble and sat near Krishna's feet.</p>
                <p>When Krishna woke up, he saw Arjuna first because Duryodhana was sitting behind him.  Krishna said, ”To one of you I will give my army and to the other, I will give my moral support because I will not fight in the war. "He let Arjun make the first choice.</p>
                <p>This is where the daily dilemma comes in. The choice we as Arjun must make every day. Do we choose the materialistic gifts or the Universal guidance? Arjuna, without wasting a second, had chosen Krishna. He knew that with the Universe's guidance, the Pandavas would certainly win the war.</p>
                <p>When Duryodhana heard Arjuna's choice, he was very happy. He was foolish enough to think that Krishna's huge army i.e. the materialistic accumulations would be of help to win the war.</p>
                <p>Each of us has a choice to be Duryodhana or to be Arjun... Duryodhana chose the materialistic world made and lost, and Arjun chose to connect with the Universe and won the war.  Each moment, we could choose the physical world or inner world as a guide to guide us further.</p>
                <p>What is it about excess indulgences that makes life more complicated in the long run? Why don't  we just simplify our life?</p>
                <p>Probably because it would change almost everything about us. It would change the way we spend our hours, our energy, and our money. It would change where we focus our attention and our minds. It would change the very foundation of our lives. Hence giving up on our materialistic, emotional and sometimes even spiritual indulgences and excesses is a scary proposition.</p>
                <p>But if we just simplify our life, it would free us up to pursue the things in life that excite us.</p>
                <p>A friend of mine, who at the age of 40, successfully exited his business is now faced with the scary thought of not having to work for money and success anymore! “So, why not work for a social cause or travel the world to have new experiences?”, I asked him. He had a humorous spin to his predicament as he replied, “Let's say, I have been bred like a rat and rat race is the only thing I have ever cared about!”</p>
                <p>Even if minimalism promises to empower us, letting go is neither culturally accepted, socially approved nor individually comprehended.</p>
