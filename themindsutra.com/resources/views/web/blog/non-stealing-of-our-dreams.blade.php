                <p>As kids, we dream of becoming Superman and Superwoman, Kings and Queens  who rule the world. As we grow older, we may continue to have a sense of entitlement. We expect dreams to materialize and then are disillusioned internally that life has turned out differently. We need to understand that just having a sense of entitlement does not get us the things we want.</p>
                <p>For many years I wanted to work on something more interesting than what I was working on. I learnt Reiki but did not practice it. I started to paint but then would give up for long periods. Not defining where I wanted to go and not taking concrete steps in any particular direction did not change my life a tad bit.</p>
                <p>I have seen a lot of younger kids who may want so much more from life ...it could be being at peace, looking better dressed, slimmer, more likable but they seem to want it as a matter of entitlement. They do not take any steps towards working towards that goal. They are not changing eating habits, working out, reading books or doing anything that would be needed for them to achieve it.</p>
                <p>Entitlement just creates a mental facade of superiority and then not achieving that creates disillusionment which is obviously expected as we have done nothing to deserve it. We need to get out of our grandioseness, our sense of entitlement and work towards our desired goal. For me , it was my friend who helped me see that I was not working towards what I wanted, that I was not consistent with what I was working on and was giving up too easily.</p>
