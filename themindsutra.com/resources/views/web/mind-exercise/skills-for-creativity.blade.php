                    <h3>Week 7: Reach out</h3>
                    <p>Reach out to people with similar interests and have lively conversations. Collective pursuits inspire us during the times we are alone and help ignite our creativity further. A mixture of solitude and collaboration that keeps us creative in art.</p>
