                <p>I recently attended a vibrant, elaborately planned Indian wedding. As I witnessed the last of the ceremonies called ‘Vidaai’, the final farewell to the bride by her family , I could not help but notice the paradox of tears amidst joy and celebrations. The teary eyed bride stepped out of the doors and threw back five handfuls of rice over her head. This ritual signifies that the bride had let go of all her current attachments in the house of her upbringing in order to start a new life.</p>
                <p>The gem of Aparigrah talks about the same experience. Aparigraha or non-possessiveness invites us to let go of our material possessions, emotional baggage or attachment to our beliefs in order to experience life in its full glory. The benefits are numerous.</p>
                <p>Most often, however, we construe the goal of life or success as accumulation. So we splurge our money on hoarding useless things and chasing worldly pleasures. In our race for power and money, we find ourselves caught  in the murky whirlpool of ethical malpractice. This is what happened to Ramalinga Raju.His story is a classic example of where this hunger of possessions can take us. In 2009, Ramalinga Raju, the former CEO of Satyam Computers had admitted to accounting fraud of over ₹7,000 crores or $1bn. Raju had started Satyam computers which, in 20 years, had become one of the Fortune 500 companies with over 50,000 employees, and reported revenues worth $2billion.Termed as India's own 'Enron Scandal', Ramalinga Raju, in his resignation letter to the Board, confessed to falsifying the company accounts of over 588 crores.</p>
                <p>'What started as a marginal gap between actual operating profit and the one reflected in the books continued to grow over the years. It has attained unmanageable proportions as the size of the company's operations grew over the years... It was like riding a tiger, not knowing how to get off without being eaten.' - Raju in his confession letter</p>
                <p>Why couldn't Raju stop accumulating wealth through unjust means once he 'started riding the tiger'?</p>
                <p>Psychology calls this phenomenon the 'paradox of rising expectations'.</p>
