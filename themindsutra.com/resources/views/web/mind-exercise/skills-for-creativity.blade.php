                    <p>Body Mind Relaxation: Find a place where you can be alone and undisturbed. Sit with your spine erect. Take a few calm deep breaths, and then relax your body by directing your attention to the sensations in your body and relaxing each muscle from head to toe. Devote 5 minutes to this each day. After one week, you may lengthen the time by 10 minutes.</p>
                    <h3>Week 3: Exercise for Creativity</h3>
                    <p>Genius hour: This exercise for fostering creativity comes from a teacher (Provenzano, 2015) who calls it a 'genius hour'. The idea is to identify a hobby (if we don't have one), start a side project, something you are passionate about. The inspiration, ideas, and skills you develop in this labour of love might well translate to other, more routine projects, moving those forward in positive ways. We will discover new skills as we go along.</p>
                    <h3>Week 4: Exercise for Synchronicity</h3>
                    <p>Connect the Dots : The time we spend with ourselves is the best time for creative inputs. This week and from now on, let's allot a dedicated time when we are with nature or ourselves. Some of the best ideas come to us during these walks with nature. Pairing these walks with journaling may be a good idea too. Having a “walk journal” that records any new ideas that come while walking. It's probably best to write any ideas down immediately after the walk so they don't slip away.</p>
